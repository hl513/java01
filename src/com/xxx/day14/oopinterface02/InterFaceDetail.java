package com.xxx.day14.oopinterface02;

public interface InterFaceDetail {

    /*
    * 接口的定义与使用：
    *      接口用关键字 interface来定义
    *        public interface 接口名 {}
    *      接口不能实例化
    *      接口和类之间的是实现关系，通过 implements关键字表示
    *         public class 类名 implements 接口名 {}
    *      接口的子类（实现类）
    *         要么重写接口的中的所有抽象方法
    *         要么是抽象类
    *
    *接口中成员的特点：
    *       成员变量
    *           只能是常量
    *           默认修饰符: public static final
    *       构造方法
    *           没有构造方法
    *       成员方法
    *           只能是抽象方法
    *           默认修饰符:public abstract
    * JDK7以前：接口中只能定义抽象方法
    * JDK8的新特性：接口中可以定义有方法体的方法
    * JDK9的新特性：接口中可以定义私有方法
    * */

}
