package com.xxx.day13.polymorphism04;

public class Polymorphism01 {
    /*
    * 封装：对象代表什么，就得封装对应的数据，并提供数据对应的行为
    * 继承：子类继承父类的，简化开发
    * 多态：对象的多种形态,同类型的对象，表现出的不同形态
    *   多态的表现形式：
    *       父类类型 对象名称 = 子类对象;
    *   多态的前提：
    *       有继承关系
    *       有父类引用指向子类对象
    *
    *   多态调用成员的特点：
    *       变量调用：编译看左边，运行也看左边
    *       方法调用：编译看左边，运行看右边
    *
    *   多态的优势：
    *       1、在多态形式下，右边对象可以实现解耦合，便于扩展和维护
    *       2、定义方法的时候，使用父类型作为参数，可以接收所有子类对象，体现多态的扩展性与便利
    * */

}
