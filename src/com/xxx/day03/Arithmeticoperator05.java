package com.xxx.day03;

// 三目运算符
public class Arithmeticoperator05 {
    public static void main(String[] args) {
        /*
         *  格式 ：关系表达式 ? 表达式1 : 表达式2
         */
        // 求两个数的较大值
        int a = 10;
        int b = 54;
        int result = a > b ? a : b;
        System.out.println(result);

    }
}
